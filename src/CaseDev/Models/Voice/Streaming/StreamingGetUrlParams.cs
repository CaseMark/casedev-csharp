using System;
using System.Collections.Frozen;
using System.Collections.Generic;
using System.Diagnostics.CodeAnalysis;
using System.Net.Http;
using System.Text.Json;
using CaseDev.Core;

namespace CaseDev.Models.Voice.Streaming;

/// <summary>
/// Returns the WebSocket URL and connection details for real-time audio transcription.
/// The returned URL can be used to establish a WebSocket connection for streaming
/// audio data and receiving transcribed text in real-time.
///
/// <para>**Audio Requirements:** - Sample Rate: 16kHz - Encoding: PCM 16-bit little-endian
/// - Channels: Mono (1 channel)</para>
///
/// <para>**Pricing:** $0.30 per minute ($18.00 per hour)</para>
/// </summary>
public sealed record class StreamingGetUrlParams : ParamsBase
{
    public StreamingGetUrlParams() { }

    public StreamingGetUrlParams(StreamingGetUrlParams streamingGetUrlParams)
        : base(streamingGetUrlParams) { }

    public StreamingGetUrlParams(
        IReadOnlyDictionary<string, JsonElement> rawHeaderData,
        IReadOnlyDictionary<string, JsonElement> rawQueryData
    )
    {
        this._rawHeaderData = [.. rawHeaderData];
        this._rawQueryData = [.. rawQueryData];
    }

#pragma warning disable CS8618
    [SetsRequiredMembers]
    StreamingGetUrlParams(
        FrozenDictionary<string, JsonElement> rawHeaderData,
        FrozenDictionary<string, JsonElement> rawQueryData
    )
    {
        this._rawHeaderData = [.. rawHeaderData];
        this._rawQueryData = [.. rawQueryData];
    }
#pragma warning restore CS8618

    /// <inheritdoc cref="IFromRawJson.FromRawUnchecked"/>
    public static StreamingGetUrlParams FromRawUnchecked(
        IReadOnlyDictionary<string, JsonElement> rawHeaderData,
        IReadOnlyDictionary<string, JsonElement> rawQueryData
    )
    {
        return new(
            FrozenDictionary.ToFrozenDictionary(rawHeaderData),
            FrozenDictionary.ToFrozenDictionary(rawQueryData)
        );
    }

    public override Uri Url(ClientOptions options)
    {
        return new UriBuilder(options.BaseUrl.ToString().TrimEnd('/') + "/voice/streaming/url")
        {
            Query = this.QueryString(options),
        }.Uri;
    }

    internal override void AddHeadersToRequest(HttpRequestMessage request, ClientOptions options)
    {
        ParamsBase.AddDefaultHeaders(request, options);
        foreach (var item in this.RawHeaderData)
        {
            ParamsBase.AddHeaderElementToRequest(request, item.Key, item.Value);
        }
    }
}
